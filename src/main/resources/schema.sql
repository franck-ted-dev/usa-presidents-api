DROP TABLE IF EXISTS "administration" CASCADE;
DROP TABLE IF EXISTS "election" CASCADE;
DROP TABLE IF EXISTS "pres_hobby" CASCADE;
DROP TABLE IF EXISTS "pres_marriage" CASCADE;
DROP TABLE IF EXISTS "president" CASCADE;

CREATE TABLE PRESIDENT (
           id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
           PRES_NAME VARCHAR(50) NOT NULL UNIQUE,
           BIRTH_YEAR INTEGER NOT NULL,
           SERV_YEAR INTEGER NOT NULL,
           DEATH_AGE INTEGER,
           PARTY VARCHAR(30) NOT NULL,
           STATE_BORN VARCHAR(30) NOT NULL
);

CREATE TABLE PRES_MARRIAGE (
           id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
           PRESIDENT_ID BIGINT NOT NULL,
           SPOUSE_NAME VARCHAR(50) NOT NULL,
           PRES_AGE INTEGER NOT NULL,       -- KORRIGIERT (war PR_AGE)
           SPOUSE_AGE INTEGER NOT NULL,     -- KORRIGIERT (war SP_AGE)
           NR_CHILDREN INTEGER NOT NULL,
           MARRIAGE_YEAR INTEGER NOT NULL,  -- KORRIGIERT (war MAR_YEAR)
           CONSTRAINT fk_president_marriage FOREIGN KEY (PRESIDENT_ID) REFERENCES PRESIDENT(id)
);

CREATE TABLE PRES_HOBBY (
            id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            PRESIDENT_ID BIGINT NOT NULL,
            HOBBY VARCHAR(100) NOT NULL,
            CONSTRAINT fk_president_hobby FOREIGN KEY (PRESIDENT_ID) REFERENCES PRESIDENT(id)
);

CREATE TABLE ELECTION (
              id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
              ELECTION_YEAR INTEGER NOT NULL,
              CANDIDATE VARCHAR(50) NOT NULL,
              VOTES INTEGER NOT NULL,
              WINNER_LOSER_INDIC CHAR(1) NOT NULL
);

CREATE TABLE ADMINISTRATION (
            id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
            PRESIDENT_ID BIGINT NOT NULL,
            VICE_PRES_NAME VARCHAR(50) NOT NULL,
            YEAR_INAUGURATED INTEGER NOT NULL,
            CONSTRAINT fk_president_admin FOREIGN KEY (PRESIDENT_ID) REFERENCES PRESIDENT(id)
);